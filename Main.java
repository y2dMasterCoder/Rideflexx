    private void viosButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viosButtonActionPerformed
        String carName = "Vios";
    int pricePerDay = 1800;

    // Get the number of days from the viosDays text field
    int daysRented;
    try {
        daysRented = Integer.parseInt(viosDays.getText().trim());
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number of days!");
        return;
    }

    // Get the table model
    javax.swing.table.DefaultTableModel tableModel = (javax.swing.table.DefaultTableModel) jTable1.getModel();

// Condition to remove the car from the table if daysRented is 0
if (daysRented == 0) {
    for (int row = 0; row < tableModel.getRowCount(); row++) {
        String existingCar = tableModel.getValueAt(row, 0).toString();
        if (existingCar.equals(carName)) {
            tableModel.removeRow(row);  // Remove the row from the table
            break;
        }
    }
    return;  // Exit the method since we're done
}

boolean found = false;

    // Check if the car already exists in the table
    for (int row = 0; row < tableModel.getRowCount(); row++) {
        String existingCar = tableModel.getValueAt(row, 0).toString();

        if (existingCar.equals(carName)) {
            // Car found, increment quantity
            int quantity = (int) tableModel.getValueAt(row, 1);
            quantity++;

            // Update table values
            tableModel.setValueAt(quantity, row, 1);                      // Update Quantity
            tableModel.setValueAt(pricePerDay, row, 2);                   // Update Price per Day (optional)
            tableModel.setValueAt(daysRented, row, 3);                    // Update Number of Days
            int totalPrice = quantity * pricePerDay * daysRented;
            tableModel.setValueAt(totalPrice, row, 4);                    // Update Total

            found = true;
            break;
        }
    }

    if (!found) {
        // Car not found, add new row
        int totalPrice = 1 * pricePerDay * daysRented;
        Object[] newRow = {carName, 1, pricePerDay, daysRented, totalPrice};
        tableModel.addRow(newRow);
    }
    }//GEN-LAST:event_viosButtonActionPerformed

